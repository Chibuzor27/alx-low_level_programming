#include <stdio.h>
#include <stddef.h>

/**
 * op_add - function
 * @a: arg
 * @b: arg
 *
 * Return: 0
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function
 * @a: arg
 * @b: arg
 *
 * Return: 0
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function
 * @a: arg
 * @b: arg
 *
 * Return: 0
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function
 * @a: arg
 * @b: arg
 *
 * Return: 0
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function
 * @a: arg
 * @b: arg
 *
 * Return: 0
 */
int op_mod(int a, int b)
{
	return (a % b);
}
